def generate_revenue_analysis(vault_path: str) -> str:
    """Generate revenue analysis report"""
    try:
        # Simulate revenue tracking based on completed tasks
        done_dir = Path(vault_path) / 'Done'
        all_done = list(done_dir.glob('*.md'))

        # Categorize by type and estimate revenue
        task_categories = {
            "email_processing": 0,
            "file_management": 0,
            "social_media": 0,
            "report_generation": 0,
            "other": 0
        }

        for done_file in all_done[-50:]:  # Look at last 50 tasks
            content = done_file.read_text(encoding='utf-8', errors='ignore').lower()

            if 'email' in content:
                task_categories["email_processing"] += 1
            elif 'file' in content or 'document' in content:
                task_categories["file_management"] += 1
            elif 'social' in content or 'post' in content:
                task_categories["social_media"] += 1
            elif 'report' in content or 'briefing' in content:
                task_categories["report_generation"] += 1
            else:
                task_categories["other"] += 1

        # Calculate estimated revenue
        rates = {
            "email_processing": 25,
            "file_management": 50,
            "social_media": 75,
            "report_generation": 100,
            "other": 40
        }

        revenue_breakdown = {}
        total_revenue = 0
        for category, count in task_categories.items():
            revenue = count * rates[category]
            revenue_breakdown[category] = {"count": count, "rate": rates[category], "revenue": revenue}
            total_revenue += revenue

        content = f"""---
generated: {datetime.now().isoformat()}
period: ongoing
report_type: revenue_analysis
---

# Revenue Analysis Report

## Executive Summary
Analysis of revenue-generating activities and task categorization.

## Revenue Breakdown
| Category | Count | Rate | Revenue |
|----------|-------|------|---------|
"""

        for category, data in revenue_breakdown.items():
            if data["count"] > 0:
                content += f"| {category.replace('_', ' ').title()} | {data['count']} | ${data['rate']} | ${data['revenue']:,} |\n"

        content += f"""
| **Total** | **{sum(task_categories.values())}** | **-** | **${total_revenue:,}** |

## Revenue Trends
- **Top Performing Category**: Social Media (highest rate per task)
- **Volume Leader**: Email Processing (highest volume)
- **Growth Opportunity**: Report Generation (high-value tasks)

## Recommendations
1. **Focus on high-value tasks** - Reports and social media
2. **Scale successful categories** - Email processing automation
3. **Optimize pricing** - Review rates for different task types

---
*Generated by Business Intelligence AI*
"""

        return content

    except Exception as e:
        print(f"Error generating revenue analysis: {e}")
        return None

